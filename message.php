<a href="">Reassuring messages</a><br />

1.	Know that the sexual violence wasn't your fault.
Whatever happened, wasn't your fault. No one has the right to sexually violet you.  The blame for a rape lies solely with the rapist. The truth is that what a person wears, what a person says, or how a person acts is never a justification for sexual violence.
Most people are raped by people they know. You do not have to protect the perpetrator. Make protecting yourself your priority. Don't worry about protecting the person who raped you.
If you want to report the crime to the police, do so. Reporting sexual violence may help protect others from that person and may help you feel a little less like you were a victim.
But making a report to the police may be difficult. If you need help reporting to police contact CSO and if you don't feel comfortable reporting it, you don't have to. You may prefer to get advice about what to do from an experienced adult who can be sympathetic to you. Do whatever helps you to feel safe and heal without blaming yourself.<br>
2.	It is critical that you seek medical care. 
40% of rape survivors develop sexually transmitted diseases as a result of sexual assault.
You should get medical attention right away without changing your clothes, showering, douching, or washing. It can be hard not to clean up, of course it's a natural human instinct to wash away all traces of a sexual assault. But being examined right away is the best way to ensure you get proper medical treatment.
Immediate medical attention helps when people decide to report the crime, providing evidence needed to prosecute the rapist if a criminal case is pursued. If you've been raped and think you don't want to report it, you could change your mind later and having the results of a medical exam can help you do this. 
Even if you don't get examined right away, it doesn't mean you can't get a checkup later. It's always best to see a doctor immediately after a rape, of course. But a person can still go to a doctor or local clinic to get checked out for STDs, pregnancy, or injuries any time after being raped. In some cases, doctors can even gather evidence several days after a rape has occurred.
A medical professional will test you for STDs, including HIV/AIDS. These tests may involve taking blood or saliva samples. Although the thought of getting an STD after a rape is extremely scary, the quicker a person finds out about any infection, the more effectively he or she can be treated. Doctors can start you on immediate treatment courses for STDs, including HIV/AIDS that will greatly increase your protection against developing these diseases.
All doctors and nurses will deal with your medical needs confidentially, and they will not inform anyone without your consent. 



